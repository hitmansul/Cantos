// Helper functions
export function findTeamCardStats(teamName: string): TeamCardStats | undefined {
  const normalized = teamName.toLowerCase().trim();
  return serieACardStats.find(t => 
    t.team.toLowerCase() === normalized ||
    t.team.toLowerCase().includes(normalized) ||
    normalized.includes(t.team.toLowerCase())
  );
}

export function calculateAvgCardsLastN(team: TeamCardStats, n: number): number {
  const games = team.last5Games.slice(0, Math.min(n, team.last5Games.length));
  if (games.length === 0) return 0;
  const total = games.reduce((sum, g) => sum + g.yellow + g.red, 0);
  return Math.round((total / games.length) * 10) / 10;
}
