// Helper functions
export function findBrazilianShotStats(teamName: string): BrazilianShotStats | undefined {
  const normalizedSearch = teamName.toLowerCase().trim();
  
  const allTeams = [...serieAShotStats, ...serieBShotStats];
  
  return allTeams.find(t => {
    const normalizedTeam = t.team.toLowerCase();
    return normalizedTeam === normalizedSearch || 
           normalizedTeam.includes(normalizedSearch) ||
           normalizedSearch.includes(normalizedTeam);
  });
}

export function calculateAvgShotsLastN(stats: BrazilianShotStats, n: number = 5): { shots: number; onTarget: number; against: number } {
  const games = stats.last5Games.slice(0, Math.min(n, stats.last5Games.length));
  if (games.length === 0) return { shots: 0, onTarget: 0, against: 0 };
  
  const totals = games.reduce(
    (acc, g) => ({ 
      shots: acc.shots + g.shots, 
      onTarget: acc.onTarget + g.onTarget,
      against: acc.against + g.against
    }), 
    { shots: 0, onTarget: 0, against: 0 }
  );
  
  return {
    shots: totals.shots / games.length,
    onTarget: totals.onTarget / games.length,
    against: totals.against / games.length,
  };
}
