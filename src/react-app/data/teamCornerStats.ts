// Helper functions
export function findTeamByName(name: string): DetailedTeamStats | undefined {
  const normalized = name.toLowerCase().trim();
  return teamStats.find(t => 
    t.team.toLowerCase() === normalized ||
    t.team.toLowerCase().includes(normalized) ||
    normalized.includes(t.team.toLowerCase())
  );
}

export function getHeadToHead(team1: string, team2: string): HeadToHead | undefined {
  return headToHeadData.find(h2h => 
    (h2h.homeTeam.toLowerCase().includes(team1.toLowerCase()) && h2h.awayTeam.toLowerCase().includes(team2.toLowerCase())) ||
    (h2h.homeTeam.toLowerCase().includes(team2.toLowerCase()) && h2h.awayTeam.toLowerCase().includes(team1.toLowerCase()))
  );
}

export function getNextMatchForTeam(teamName: string): NextMatch | undefined {
  const normalized = teamName.toLowerCase();
  return upcomingMatches.find(m => 
    m.homeTeam.toLowerCase().includes(normalized) || 
    m.awayTeam.toLowerCase().includes(normalized)
  );
}

export function calculateAvgLastNGames(team: DetailedTeamStats, n: number): number {
  const games = team.last5Games.slice(0, Math.min(n, team.last5Games.length));
  if (games.length === 0) return 0;
  return Math.round((games.reduce((a, b) => a + b, 0) / games.length) * 10) / 10;
}
