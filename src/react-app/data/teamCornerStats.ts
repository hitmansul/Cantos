/**
 * Detailed corner statistics for Brazilian teams
 * All stats are per-game averages based on 2024 season data
 */

export interface DetailedTeamStats {
  team: string;
  league: string;
  logoUrl?: string;
  
  // General stats
  gamesPlayed: number;
  avgCornersFor: number;        // Average corners taken per game
  avgCornersAgainst: number;    // Average corners conceded per game
  avgTotalCorners: number;      // Average total corners in their games
  
  // Home/Away breakdown
  homeGames: number;
  awayGames: number;
  avgCornersHome: number;       // Média jogando em casa
  avgCornersAway: number;       // Média jogando fora
  
  // By match situation
  avgCornersWinning: number;    // Média quando está ganhando
  avgCornersLosing: number;     // Média quando está perdendo
  avgCornersDrawing: number;    // Média quando está empatando
  
  // By time period
  avgCornersFirstHalf: number;  // Média no 1º tempo
  avgCornersSecondHalf: number; // Média no 2º tempo
  
  // Recent form (last 5 games)
  last5Games: number[];         // Corners in last 5 games
  avgLast5: number;             // Média nos últimos 5 jogos
  
  // Additional stats
  firstCornerPct: number;       // % of games where they took first corner
  over85Pct: number;
  over95Pct: number;
  over105Pct: number;
}

export interface HeadToHead {
  homeTeam: string;
  awayTeam: string;
  matches: {
    date: string;
    homeScore: number;
    awayScore: number;
    homeCorners: number;
    awayCorners: number;
    totalCorners: number;
  }[];
  avgTotalCorners: number;      // Média de escanteio no confronto direto
  avgHomeCorners: number;
  avgAwayCorners: number;
}

export interface NextMatch {
  homeTeam: string;
  awayTeam: string;
  date: string;
  competition: string;
  predictedCorners: number;
}

